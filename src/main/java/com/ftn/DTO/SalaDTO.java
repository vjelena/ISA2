package com.ftn.DTO;


public class SalaDTO {
	
	
	private String nazivSale;
	
	
	private int brojMesta;
	
	
	private String konfiguracija;
	
	
	private String bioskopId;


	public String getNazivSale() {
		return nazivSale;
	}


	public void setNazivSale(String nazivSale) {
		this.nazivSale = nazivSale;
	}


	public int getBrojMesta() {
		return brojMesta;
	}


	public void setBrojMesta(int brojMesta) {
		this.brojMesta = brojMesta;
	}


	public String getKonfiguracija() {
		return konfiguracija;
	}


	public void setKonfiguracija(String konfiguracija) {
		this.konfiguracija = konfiguracija;
	}


	public String getBioskopId() {
		return bioskopId;
	}


	public void setBioskopId(String bioskopId) {
		this.bioskopId = bioskopId;
	}
	
	
	

}
